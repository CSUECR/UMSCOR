        /// 用于服务端验证QQ是否合法
        /// </summary>
        /// <param name="data">待验证数据</param>
        /// <returns>0-合法，1-不合法</returns>        
        public static int ValidateQQ(string data)
        {
            if (Regex.IsMatch(data, qq) || Regex.IsMatch(data, emp))
            {
                return 0;
            }
            else
            {
                return 1;
            }
        }

