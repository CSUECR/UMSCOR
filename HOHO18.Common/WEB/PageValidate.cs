        #region 自己的

        /// <summary>
        /// 用于验证用户名
        /// </summary>
        /// <param name="data">待验证数据</param>
        /// <returns>0-合法,1不合法</returns>        
        public static int ValidateUsername(string data)
        {
            if (Regex.IsMatch(data, username))
            {
                return 0;
            }
            else
            {
                return 1;
            }
        }

        /// <summary>
        /// 用于验证密码
        /// </summary>
        /// <param name="data">待验证数据</param>
        /// <returns>0-合法</returns>        
        public static int ValidatePassword(string data)
        {
            if (data.Length < 6 || data.Length > 16)
            {
                return 1;
            }
            else
            {
                return 0;
            }
        }

        /// <summary>
        /// 用于验证密码,可以为空的状况
        /// </summary>
        /// <param name="data">待验证数据</param>
        /// <returns>0-合法,1-不合法</returns>        
        public static int ValidateUpPassword(string data)
        {
            if (String.IsNullOrEmpty(data))
            {
                return 0;
            }
            if (data.Length < 6 || data.Length > 16)
            {
                return 1;
            }
            else
            {
                return 1;
            }
        }

        /// <summary>
        /// 用于验证两次输入的密码是否相同
        /// </summary>
        /// <param name="data">待验证数据</param>
        /// <param name="data">比较数据</param>
        /// <returns>0-一致，1-不一致</returns>        
        public static int ValidateCheckPassword(string data, string checkData)
        {
            if (String.Compare(data, checkData) == 0)
            {
                return 0;
            }
            else
            {
                return 1;
            }
        }

